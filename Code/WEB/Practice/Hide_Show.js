            function resetDisplay(){
                var con = document.getElementById("reset");
                if(con.style.display!='none'){
                    con.style.display='none';
                }
            }
            function firstDisplay(){
                var con = document.getElementById("first");
                if(con.style.display=='none'){
                    con.style.display='block';
                }else{
                    con.style.display='none';
                }
            }
